package operator.practice;

public class Q05 {
    public static void main(String[] args) {
        int num1 = 2;
        int num2 = 10;

        System.out.println(num1 & num2); // 2
        System.out.println(num1 | num2); // 10
        System.out.println(num1 ^ num2); // 8
        System.out.println(~num1); // -3

    }
}
